//  Array : Stores multiple values in a single variable

// Example1 
let colors = ['Blue', 'Black', 'Orange', 'Yellow', 'Green', 'White']

// Printing Array Values  bye indexes
console.log(colors); // print whole array values

// Array Index : get specific val
console.log(colors[0]) // Blue
console.log(colors[4]) // Green
console.log(colors[2]) // ?
console.log(colors[10]) // undefined 


//  Array length : number of elements in the array
console.log(colors.length) // 6




